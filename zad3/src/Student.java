public class Student {
    String fname;
    String lname;
    int indexNumber;
    String email;
    String address;
    int grade;
    public Student(String fname, String lname, int indexNumber, String email, String address, int grade) {
        this.fname = fname;
        this.lname = lname;
        this.indexNumber = indexNumber;
        this.email = email;
        this.address = address;
        this.grade = grade;
    }
}
